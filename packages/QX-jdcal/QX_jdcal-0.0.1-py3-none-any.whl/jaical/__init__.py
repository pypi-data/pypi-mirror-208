import numpy as np
def add_numbers(num1, num2):
    return num1 + num2

def subtract_numbers(num1, num2):
    return num1 - num2

def multiply_numbers(num1, num2):
    return num1 * num2

def divide_numbers(num1, num2):
    return num1 / num2

def deg_to_Rad(num1):
    return np.deg2rad(num1)

def power(num1,num2):
    return np.power(num1,num2)